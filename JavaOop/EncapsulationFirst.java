package JavaOop;

public class EncapsulationFirst {
    private int year = 1994;
    private String month = "August";

    public int getYear() {
        return year;
    }
    public void setYear(int newYear) {
        this.year = newYear;
    }

    public String getMonth() {
        return month;
    }

    public void setMonth(String newMonth) {
        this.month = newMonth;
    }
}
