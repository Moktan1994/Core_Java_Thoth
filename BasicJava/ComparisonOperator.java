package BasicJava;

public class ComparisonOperator {
    public static void main(String[] args) {
        int x = 50;
        int y = 40;
        System.out.println(x);

        System.out.println(x == y);
        System.out.println(x != y);
        System.out.println(x > y);
        System.out.println(x < y);
        System.out.println(x >= y);
        System.out.println(x <= y);
    }
}
