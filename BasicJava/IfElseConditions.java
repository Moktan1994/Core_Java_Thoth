package BasicJava;

public class IfElseConditions {
    public static void main(String[] args) {

        int x = 15;
        int y = 20;

        if (x > y) {
            System.out.println("Greater");
        }
        if (x < y) {
            System.out.println("Smaller");
        }
        else {
            System.out.println("Equal");

        }
    }
}
